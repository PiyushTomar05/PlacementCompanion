export function getNextTopicForSubject(subjectName, completedTopicNames = []) {
  const roadmap = MASTER_ROADMAP[subjectName] || [];
  if (roadmap.length === 0) return null;

  const cleanStr = (s) => String(s).trim().toLowerCase().replace(/[^a-z0-9]/g, '');
  const completedSet = new Set(completedTopicNames.map(cleanStr));

  for (let i = 0; i < roadmap.length; i++) {
    const topic = roadmap[i];
    if (!completedSet.has(cleanStr(topic))) {
      return { topicName: topic, topicIndex: i, totalTopics: roadmap.length };
    }
  }

  return { topicName: roadmap[roadmap.length - 1], topicIndex: roadmap.length - 1, totalTopics: roadmap.length };
}
